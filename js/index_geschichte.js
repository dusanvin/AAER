function getDate() {
    var d = new Date();
    var month = d.getMonth() + 1;
    var day = d.getDate();
    return (('' + day).length < 2 ? '0' : '') + day + '.' + (('' + month).length < 2 ? '0' : '') + month + '.' + d.getFullYear();
}


